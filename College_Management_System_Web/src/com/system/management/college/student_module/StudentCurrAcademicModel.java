package com.system.management.college.student_module;

public class StudentCurrAcademicModel {
	
	private String studentId;
	private String departmentId;
	private int currentSem;
	private float sem1Marks;
	private float sem2Marks;
	private float sem3Marks;
	private float sem4Marks;
	
	
	public String getStudentId() {
		return studentId;
	}
	
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	
	public String getDepartmentId() {
		return departmentId;
	}
	
	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}
	
	public int getCurrentSem() {
		return currentSem;
	}
	
	public void setCurrentSem(int currentSem) {
		this.currentSem = currentSem;
	}
	
	public float getSem1Marks() {
		return sem1Marks;
	}
	
	public void setSem1Marks(float sem1Marks) {
		this.sem1Marks = sem1Marks;
	}
	
	public float getSem2Marks() {
		return sem2Marks;
	}
	public void setSem2Marks(float sem2Marks) {
		this.sem2Marks = sem2Marks;
	}
	
	public float getSem3Marks() {
		return sem3Marks;
	}
	
	public void setSem3Marks(float sem3Marks) {
		this.sem3Marks = sem3Marks;
	}
	
	public float getSem4Marks() {
		return sem4Marks;
	}
	
	public void setSem4Marks(float sem4Marks) {
		this.sem4Marks = sem4Marks;
	}
	
	

}
